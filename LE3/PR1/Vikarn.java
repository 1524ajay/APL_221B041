
class Vikarn extends Kaurav implements Obedient, Kindness {
    @Override
    public void fight() {
        System.out.println("Vikarn fights like a noble warrior.");
    }

    @Override
    public void obey() {
        // Implementing obey() method as required by the Obedient interface
        System.out.println("Vikarn obeys like a true Bharatvanshi.");
    }

    @Override
    public void kind() {
        System.out.println("Vikarn is kind and just.");
    }
}

// Pandav.java
class Pandav extends Bharatvanshi implements Obedient, Kindness {
    @Override
    public void fight() {
        System.out.println("Pandav fights bravely!");
    }

    @Override
    public void obey() {
        System.out.println("Pandav obeys without question.");
    }

    @Override
    public void kind() {
        System.out.println("Pandav is kind.");
    }
}
